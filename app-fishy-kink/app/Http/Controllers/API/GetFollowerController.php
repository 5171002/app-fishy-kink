        $users = [];
        $followerList = $db["userDB"]->findOne(["userID" => $userID]);
        foreach($followerList["follower"] as $follower){
            $users[] = $db["userDB"]->findOne(["userID" => $follower]);
            // $users = $db["userDB"]->findOne(["userID" => $follow]);
        }
        return ["follower"=> $users];
    }

